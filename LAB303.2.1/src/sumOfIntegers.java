public class sumOfIntegers {
    public static class SumOfIntegers {
        public static void main(String[] args) {
            // Declare two integer variables
            int num1 = 10;
            int num2 = 20;

            // Calculate the sum
            int sum = num1 + num2;

            // Print out the result
            System.out.println("Sum of " + num1 + " and " + num2 + " is: " + sum);
        }
    }

}
